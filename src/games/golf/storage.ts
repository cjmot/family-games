export const golfRules = `
GOAL
Have the lowest total score when the game ends.

SETUP
• For 3-4 players, use a standard 52-card deck. For 5-6 players, add a second deck.
• Each player is dealt 6 cards face down in a 3x2 grid.
• The remaining deck is placed face down, with one card flipped face up to start the discard pile.

CARD VALUES
• Ace = 1
• 2–10 = Face value
• Jack = 10
• Queen = 10
• King = 0
• Jokers (optional) = −2

SPECIAL RULES
• Pairs in the same column cancel out and score 0 points.
• If both cards in a column match, the column is removed from scoring.

TURN ORDER
On your turn, you may:
1) Draw the top card from the deck OR take the top discard.
2) Swap it with one of your face-down cards (discarding the replaced card), OR
3) Discard the drawn card without swapping.

ENDING THE ROUND
• Once a player has all their cards face up, all other players get one final turn and the round ends.
• On your final turn, if you still have cards face-down after drawing and replacing, you must flip them face up.

WINNING
• Play multiple rounds.
• The player with the lowest total score after all rounds wins.
`
